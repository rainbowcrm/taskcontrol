CREATE TABLE BUSINESS_PROFILES (
PROFILE_CODE VARCHAR(25) NOT NULL UNIQUE,
PROFILE_DESC VARCHAR(50) NOT NULL UNIQUE,
INDUSTRY VARCHAR(200) NOT NULL ,
PROFILE_ACCESS_CODE VARCHAR(750),
PARENT_PROFILE_CODE VARCHAR(25)
);

CREATE TABLE MODULES (
MODULE_CODE VARCHAR(10) NOT NULL UNIQUE,
MODULE_DESC VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE COMPANY_PROFILES (
PROFILE_CODE VARCHAR(10) NOT NULL UNIQUE,
PROFILE_DEC VARCHAR(100) NOT NULL UNIQUE,
BUSINESS_PROFILE_CODE VARCHAR(10) NOT NULL UNIQUE,
PARENT_PROFILE_CODE VARCHAR(10) NOT NULL UNIQUE
) ;



CREATE TABLE COMPANIES (
ID INT PRIMARY KEY AUTO_INCREMENT,
COMPANY_CODE VARCHAR(25) NOT NULL UNIQUE,
COMPANY_NAME VARCHAR(100) NOT NULL UNIQUE,
ADDRESS1 VARCHAR(100),
ADDRESS2 VARCHAR(100),
ZIPCODE VARCHAR(50),
CITY VARCHAR(50),
PHONE    VARCHAR(20),
BUSINESS_PROFILE_CODE VARCHAR(25),
IS_ACTIVE BOOLEAN DEFAULT TRUE ,
IS_DELETED BOOLEAN DEFAULT FALSE,
CREATED_DATE DATETIME,
LAST_UPDATED_DATE DATETIME,
CREATED_BY VARCHAR(50),
LAST_UPDATED_BY VARCHAR(50),
FOREIGN KEY (BUSINESS_PROFILE_CODE) REFERENCES BUSINESS_PROFILES(PROFILE_CODE)
);



CREATE TABLE USERS (
USER_ID VARCHAR(50) PRIMARY KEY,
COMPANY_ID INT,
PASSWORD VARCHAR(250) NOT NULL,
FIRST_NAME VARCHAR(50),
LAST_NAME VARCHAR(50),
PHONE VARCHAR(50) NOT NULL UNIQUE ,
EMAIL VARCHAR(50) NOT NULL UNIQUE,
CREATED_DATE DATETIME,
LAST_UPDATED_DATE DATETIME,
CREATED_BY VARCHAR(50),
LAST_UPDATED_BY VARCHAR(50),
FOREIGN KEY (COMPANY_ID) REFERENCES COMPANIES(ID)
);
